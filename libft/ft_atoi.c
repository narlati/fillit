int		ft_atoi(char *c)
{
	int ret;
	int s;

	ret = 0;
	s = 0;
	while (*c != '\0')
	{
		if (*c >= '0' && *c <= '9')
		{
			ret = 10 * ret + (*c - '0');
			s = ((s == 0) ? 1 : s);
		}
		else if (*c == '+' && s == 0 && ret == 0)
			s = 1;
		else if (*c == '-' && s == 0 && ret == 0)
			s = -1;
		else if (ret == 0 && s == 0 && (*c == ' ' || *c == '\t' || *c == '\n' ||
					*c == 13 || *c == 12 || *c == '\v' || *c == '\r'))
			;
		else
			break ;
		c++;
	}
	return ((s == 0) ? ret : s * ret);
}
